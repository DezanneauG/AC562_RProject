  g_eu <- g_eu + edge("Germany","Denmark")
  g_eu <- g_eu + edge("Germany","Netherlands")
  g_eu <- g_eu + edge("Germany","Belgium")
  g_eu <- g_eu + edge("Germany","France")
  g_eu <- g_eu + edge("Germany","Switzerland")
  g_eu <- g_eu + edge("Germany","Austria")
  g_eu <- g_eu + edge("Germany","Czech Republic")
  g_eu <- g_eu + edge("Germany","Poland")
  g_eu <- g_eu + edge("Netherlands","Belgium")
