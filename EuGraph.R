'===== libraries ====='
library(igraph)

source("EuNames.R")

'===== functions definition ====='
'Europe Graph'
EuGraph <- function() {
  #Noms des Pays
  Names = EuNames()
  L = length(Names)
  
  #création d'un graph vide
  g_eu <- graph.empty()
  #ajout de 36 nodes
  g_eu <- add.vertices(g_eu, L)
  #nommage des nodes
  V(g_eu)$name <- Names[1:L]
  #ajout des liens
  g_eu <- g_eu + edge("Finland","Norway")
  g_eu <- g_eu + edge("Finland","Sweden")
  g_eu <- g_eu + edge("Finland","Russia")
  g_eu <- g_eu + edge("Russia","Sweden")
  g_eu <- g_eu + edge("Norway","Sweden")
